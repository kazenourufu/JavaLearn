package _TEMP;

public class People {
    int age = 5;
    String name = "小明";

    public People(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge(){
        return this.age;
    }

    public String getName() {
        return name;
    }
}
